		<h4>What is Ecash?</h4>
		<p>
			Ecash is a digital currency that was developed in the early 1990s by David Chaum, who is
			considered one of the pioneers in cryptography and electronic cash systems. It is essentially
			a form of digital money designed to be secure, anonymous, and decentralized. Ecash operates
			using a combination of public key cryptography and blind signatures to ensure privacy and
			security during transactions. Users can create "blinded" versions of their public keys,
			allowing them to make purchases without revealing their identity. This process ensures that
			both parties involved in a transaction remain anonymous while still maintaining trust between
			them. The concept behind ecash has influenced many subsequent developments in the field of
			digital currencies, including Bitcoin and other cryptocurrencies. However, ecash itself never
			gained widespread adoption due to various technical challenges and operational failures
		</p>
		<h4>What is Cashu?</h4>
		<p>
			Cashu is a new ecash protocol for custodial Bitcoin applications that is tightly integrated
			into the Lightning network. An Ecash system consists of two parts, the mint and the ecash
			wallet. Ecash transactions are untraceable, instant, and require no fees. Cashu is built for
			Bitcoin. Wallets use the mint's Lightning node for making or receiving Bitcoin payments in
			exchange for ecash. A Cashu mint does not know who you are, what your balance is, or who
			you're transacting with. Users of a mint can exchange ecash privately without anyone being
			able to know who the involved parties are. Bitcoin payments are executed without anyone able
			to censor specific users.
		</p>
